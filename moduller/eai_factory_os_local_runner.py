# HTML dosyas覺n覺 olutur
FILE_NAME = "index.html"
PORT = 8000

def start_server():
    # Mevcut dizinde index.html olutur
    with open(FILE_NAME, "w", encoding="utf-8") as f:
        f.write(html_content)

    Handler = http.server.SimpleHTTPRequestHandler

    with socketserver.TCPServer(("", PORT), Handler) as httpd:
        print(f"\\n[!] EAI Factory OS Yerel Sunucusu Balat覺ld覺.")
        print(f"[!] Adres: http://localhost:{PORT}")
        print(f"[!] Kapatmak i癟in terminalde Ctrl+C tular覺na bas覺n.\\n")

        # Taray覺c覺y覺 1 saniye sonra a癟
        threading.Timer(1.5, lambda: webbrowser.open(f"http://localhost:{PORT}")).start()

        try:
            httpd.serve_forever()
        except KeyboardInterrupt:
            print("\\n[x] Sunucu kapat覺l覺yor...")
            httpd.server_close()

if __name__ == "__main__":
    start_server()

